package com.allteran.deliverer.domain;

public class FoodCategory {
    private String id;
    private String name;

    public FoodCategory() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
